
//Númros inteiros/reais
package Principal;

public class Programa12 {

	public static void main(String[] args) {
	// Tipos primáris primitivos.
	//Por padrão os números reais em java são são considerados double.
		
		float preco1 = 23.4f; //23.40
		double preco2 = 23.4; //23.43434343434343434343
		
	//tipos não primários primitivos
		Float preco3 = 44.5f;
		Double preco4 = 44.5;
		
		System.out.println("float -> " + preco1);
		System.out.println("Double -> " + preco2);
		System.out.println("Float -> " + preco3);
		System.out.println("Double -> " + preco4);

	}

}
